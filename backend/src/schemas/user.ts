import * as mongoose from 'mongoose';

export default mongoose.model(
  'User',
  new mongoose.Schema({
    id: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
    name: {
      type: String,
      required: true,
    },
    createdAt: {
      type: Date,
      default: Date.now,
    },
  }),
);
